package org.pos2d.webapi.entities;

public class maplayout {
	
	private int gid;
	private double  xcordinate;
	private double  ycordinate;
	private double latitude;
	private double longitude;
	private double perunitkm;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public double getXcordinate() {
		return xcordinate;
	}
	public void setXcordinate(double xcordinate) {
		this.xcordinate = xcordinate;
	}
	public double getYcordinate() {
		return ycordinate;
	}
	public void setYcordinate(double ycordinate) {
		this.ycordinate = ycordinate;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public double getPerunitkm() {
		return perunitkm;
	}
	public void setPerunitkm(double perunitkm) {
		this.perunitkm = perunitkm;
	}
	

}
